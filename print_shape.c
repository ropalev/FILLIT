#include "fillit.h"

void		print_shape(int **map, int size)
{
	int		i;
	int		j;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size)
		{
			if (map[i][j] == 0)
				write(1, ".", 1);
			else
				write(1, &map[i][j], 1);
			j++;
		}
		i++;
		write(1, "\n", 1);
	}
}
